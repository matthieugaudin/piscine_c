int	ft_is_prime(int nb)
{
	int	num;

	if (nb <= 0 || nb == 1)
		return (0);
	num = 2;
	while (num < nb / 2)
	{
		if (nb % num == 0)
			return (0);
		num++;
	}
	return (1);
}
