#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int	*tab;
	int	tab_len;
	int	i;

	tab_len = max - min;
	tab = malloc(tab_len * sizeof(int));
	if (min >= max)
		return (NULL);
	i = 0;
	while (min < max)
	{
		tab[i] = min;
		min++;
		i++;
	}
	return (tab);
}
